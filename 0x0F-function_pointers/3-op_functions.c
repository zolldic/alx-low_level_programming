/**
* op_add - a function the calculate the sum of two integers
* @a: first number
* @b: second number
* Return: the sum of a and b
*/
int op_add(int a, int b)
{
	return (a + b);
}

/**
* op_sub - a function the calculate the difference between two integers
* @a: first number
* @b: second number
* Return: the difference of a and b
*/
int op_sub(int a, int b)
{
	return (a - b);
}

/**
* op_mul - a function the calculate the multiplication of two integers
* @a: first number
* @b: second number
* Return: the product of a and b.
*/
int op_mul(int a, int b)
{
	return (a * b);
}

/**
* op_div - a function the calculate the division of two integers
* @a: first number
* @b: second number
* Return: the division of a by b.
*/
int op_div(int a, int b)
{
	return (a / b);
}


/**
* op_mod - a function the calculate the remainder of two integers
* @a: first number
* @b: second number
* Return: the remainder of the division of a by b.
*/
int op_mod(int a, int b)
{
	return (a % b);
}
